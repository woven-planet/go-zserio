// Package ztype provides (de)serialization functions for zserio primitive
// types.
//
// Zserio defines a set of primitive types such as integer in various sizes,
// strings, floats and arrays. This package provides functions to read and
// write those types.
//
// Normally you will never need to use this package directly. It is used by
// code that generated from zserio schemas.
package ztype
